# find_the_duplicate.rb

# PROBLEM
# Input: An array with unique values and one duplicated value
# Output: The duplicated value
# Data type unknown (all examples have integers)

# EXAMPLES
# find_dup([1, 5, 3, 1]) == 1
# find_dup([18,  9, 36, 96, 31, 19, 54, 75, 42, 15,
#           38, 25, 97, 92, 46, 69, 91, 59, 53, 27,
#           14, 61, 90, 81,  8, 63, 95, 99, 30, 65,
#           78, 76, 48, 16, 93, 77, 52, 49, 37, 29,
#           89, 10, 84,  1, 47, 68, 12, 33, 86, 60,
#           41, 44, 83, 35, 94, 73, 98,  3, 64, 82,
#           55, 79, 80, 21, 39, 72, 13, 50,  6, 70,
#           85, 87, 51, 17, 66, 20, 28, 26,  2, 22,
#           40, 23, 71, 62, 73, 32, 43, 24,  4, 56,
#           7,  34, 57, 74, 45, 11, 88, 67,  5, 58]) == 73

# DATA STRUCTURES
# A helper value to store duplicate

# ALGORITHM
# Sort the array
# Initialize index = 0
# Initialize value = array[index]
# Iterate through the array.
# On each iteration:
#   If array[index + 1] == array[index]
#     Return value
#   index += 1
#   value = array[index]

# CODING
def find_dup(nums)
  nums_sorted = nums.sort
  nums_sorted.select.with_index { |num, idx| num == nums_sorted[idx + 1] }.last
end

